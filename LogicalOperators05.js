// Logical AND (&&) Operator:
let a = 5, b = 4;
console.log(a < 6 && b > 3); //true
console.log(a < 6 && b < 3); //false

// Logical OR (||) Operator:
let a = 5, b = 4;
console.log(a == 6 || b == 3);//false
console.log(a == 5 || b == 0);//true
console.log(a == 0 || b == 4);//true
console.log(a == 5 || b == 4);//true

// Logical Not (!) Operator
let a = 5, b = 4;
console.log(!(a == b));//true
console.log(!(a > b));//false

